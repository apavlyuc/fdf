int			words_count(char const *s, char del)
{
	int		count;
	int		i;

	i = 0;
	count = 0;
	while (*(s + i) != '\0')
	{
		while (*(s + i) == del)
			i++;
		if (*(s + i) != '\0')
			count++;
		while (*(s + i) != del && *(s + i) != '\0')
			i++;
	}
	return (count);
}
