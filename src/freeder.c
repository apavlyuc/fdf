#include "fdf.h"
#include <stdlib.h>

void	free_vec3(t_vector3 ***vec3, int size)
{
	int	i;

	i = -1;
	while (++i < size)
	{
		free(*(*vec3 + i));
	}
	free(*vec3);
}
